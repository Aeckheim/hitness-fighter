// Sprite rendering helper - draws a pixel grid at given scale
function drawSprite(ctx, sprite, x, y, scale, flipped) {
    const h = sprite.length;
    const w = sprite[0].length;
    ctx.save();
    if (flipped) {
        ctx.translate(x + w * scale, y);
        ctx.scale(-1, 1);
    } else {
        ctx.translate(x, y);
    }
    for (let row = 0; row < h; row++) {
        for (let col = 0; col < w; col++) {
            const color = sprite[row][col];
            if (color && color !== 0) {
                ctx.fillStyle = color;
                ctx.fillRect(col * scale, row * scale, scale, scale);
            }
        }
    }
    ctx.restore();
}

